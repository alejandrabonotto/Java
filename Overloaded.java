package exercise03;

public class Overloaded {
	
	public int sum(int a, int b) {
		int sum = a + b;
		return sum;
	}
	
	public int sum(int a, int b, int c) {
		int sum = a + b + c;
		return sum;
	}
	
	public int sum(int a, int b, int c, int d) {
		int sum = a + b + c+ d;
		return sum;
	}
	
}
